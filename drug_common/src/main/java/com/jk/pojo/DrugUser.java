package com.jk.pojo;

public class DrugUser {
    private Integer userId;
    private String userName;
    private String userPassword;
    private String userAccoountNumber;
    private String userPhoneNumber;

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getUserPassword() {
        return userPassword;
    }

    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }

    public String getUserAccoountNumber() {
        return userAccoountNumber;
    }

    public void setUserAccoountNumber(String userAccoountNumber) {
        this.userAccoountNumber = userAccoountNumber;
    }

    public String getUserPhoneNumber() {
        return userPhoneNumber;
    }

    public void setUserPhoneNumber(String userPhoneNumber) {
        this.userPhoneNumber = userPhoneNumber;
    }
}
